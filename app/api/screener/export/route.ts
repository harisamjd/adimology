export async function GET(req: NextRequest) {
  const url = new URL(req.url);
  const format = url.searchParams.get("format") || "text"; // text | html | json
  const mode = url.searchParams.get("mode") || "combo";
  const date = url.searchParams.get("date") || new Date().toISOString().split("T")[0];
  const limit = parseInt(url.searchParams.get("limit") || "30");

  const { data, error } = await supabase
    .from("screener_results")
    .select("*")
    .eq("screener_mode", mode)
    .gte("created_at", `${date}T00:00:00`)
    .lte("created_at", `${date}T23:59:59`)
    .order("score", { ascending: false })
    .limit(limit);

  if (error) return NextResponse.json({ error: error.message }, { status: 500 });
  if (!data?.length) return NextResponse.json({ error: "Tidak ada data untuk tanggal ini", date, mode }, { status: 404 });

  if (format === "text") {
    const text = buildTelegramText(data, date, mode);
    return new Response(text, {
      headers: {
        "Content-Type": "text/plain; charset=utf-8",
        "Content-Disposition": `attachment; filename="trading-plan-${date}.txt"`,
      },
    });
  }

  if (format === "html") {
    const html = buildHtmlPdf(data, date, mode);
    return new Response(html, {
      headers: {
        "Content-Type": "text/html; charset=utf-8",
        "Content-Disposition": `inline; filename="trading-plan-${date}.html"`,
      },
    });
  }

  return NextResponse.json({ results: data, date, mode, total: data.length });
}
