const supabase = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL!,
  process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!
);

function formatRupiah(n: number): string {
  return n?.toLocaleString("id-ID") || "0";
}

function formatDate(isoDate: string): string {
  return new Date(isoDate).toLocaleDateString("id-ID", {
    weekday: "long", day: "numeric", month: "long", year: "numeric",
  });
}

function buildTelegramText(results: Array<{
  ticker: string;
  score: number;
  priority: string;
  signals: string[];
  trading_plan: {
    entryLow: number; entryHigh: number;
    tp1: number; tp2: number;
    cutLoss: number; rrRatio: number;
  };
  avg_bandar: number;
  rsi: number;
  volume_multiplier: number;
  screener_mode: string;
}>, date: string, mode: string): string {
  const dateStr = formatDate(date + "T00:00:00");
  const modeLabel: Record<string, string> = {
    combo: "Combo Score", bandar: "Akumulasi Bandar", oversold: "Oversold/Bounce",
    volume_spike: "Volume Spike", breakout: "Breakout", bsjp: "BSJP",
  };

  let text = `📋 *TRADING PLAN HARIAN*\n`;
  text += `📅 ${dateStr}\n`;
  text += `🎯 Mode: ${modeLabel[mode] || mode.toUpperCase()}\n`;
  text += `${"━".repeat(30)}\n\n`;

  const high = results.filter(r => r.priority === "HIGH");
  const medium = results.filter(r => r.priority === "MEDIUM");
  const low = results.filter(r => r.priority === "LOW");

  function addSection(label: string, items: typeof results, emoji: string) {
    if (!items.length) return;
    text += `${emoji} *${label}* (${items.length} saham)\n`;
    text += `${"─".repeat(25)}\n`;
    for (const r of items) {
      const plan = r.trading_plan;
      const upside1 = plan.tp1 && r.trading_plan.entryHigh
        ? (((plan.tp1 - r.trading_plan.entryHigh) / r.trading_plan.entryHigh) * 100).toFixed(1)
        : "0.0";
      const downside = plan.cutLoss && r.trading_plan.entryLow
        ? (((plan.cutLoss - r.trading_plan.entryLow) / r.trading_plan.entryLow) * 100).toFixed(1)
        : "0.0";

      text += `\n*${r.ticker}* — Skor ${r.score}/100\n`;
      text += `Sinyal: ${r.signals?.slice(0, 2).join(", ")}\n`;
      text += `Entry : ${formatRupiah(plan.entryLow)}–${formatRupiah(plan.entryHigh)}\n`;
      text += `TP1   : ${formatRupiah(plan.tp1)} (+${upside1}%)\n`;
      text += `TP2   : ${formatRupiah(plan.tp2)}\n`;
      text += `CL    : ${formatRupiah(plan.cutLoss)} (${downside}%)\n`;
      text += `R/R   : 1:${plan.rrRatio} | RSI: ${r.rsi?.toFixed(0) || "-"}\n`;
      if (r.avg_bandar) text += `Avg Bandar: ${formatRupiah(r.avg_bandar)}\n`;
    }
    text += "\n";
  }

  addSection("HIGH PRIORITY", high, "🔥");
  addSection("MEDIUM PRIORITY", medium, "⚡");
  addSection("LOW PRIORITY", low, "📌");

  text += `${"━".repeat(30)}\n`;
  text += `_Generated by Adimology Screener_`;

  return text;
}

